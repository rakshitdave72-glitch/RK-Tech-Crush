// Primary: screenshotof.com — 1000 free req/day, no API key
export function getThumb(url: string) {
  const clean = url.replace(/\/$/, "");
  return `https://screenshotof.com/${clean}`;
}

// Secondary fallback: pagepeeker free tier
export function getThumb2(url: string) {
  const clean = url.replace(/\/$/, "");
  return `http://free.pagepeeker.com/v2/thumbs.php?size=x&url=${encodeURIComponent(clean)}`;
}

